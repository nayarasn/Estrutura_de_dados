# 4. Crie um programa que leia uma lista de números e exiba o maior e o menor valor da lista

lista = [5, 1, 10, 16, 50, 2, 25]
maior_valor = max(lista)
menor_valor = min(lista)

print(f"A lista é composta pelos seguintes números: {lista} | Seu maior valor é {maior_valor} e o menor valor é {menor_valor}")